package app.entity;

public class Message {
    private String messageId;
    private String from;
    private String to;
    private String content;

    public String getMessageId() {
        return messageId;
    }

    public String getFrom() {
        return from;
    }

    public String getTo() {
        return to;
    }

    public String getContent() {
        return content;
    }
}
